// Simple text translation helper for long sentences / objects
export function translateBackendText(text, lang) {
  if (!text) return text;
  if (lang === 'en') return text;
  
  // Exact match
  if (backendTranslations[lang][text]) {
    return backendTranslations[lang][text];
  }
  
  // Color names match (word boundary)
  let translatedText = text;
  const terms = Object.keys(backendTranslations[lang]).sort((a,b) => b.length - a.length);
  for (const term of terms) {
    // Only replace if it's the beginning, end, or surrounded by spaces/punctuation
    const regex = new RegExp(`\\b${term}\\b`, 'gi');
    translatedText = translatedText.replace(regex, (match) => {
      // preserve casing if possible
      const translated = backendTranslations[lang][term];
      return match[0] === match[0].toUpperCase() ? 
        translated.charAt(0).toUpperCase() + translated.slice(1) : 
        translated.toLowerCase();
    });
  }
  
  return translatedText;
}

// Translate deep objects from backend (arrays of objects, etc)
export function translateBackendObject(obj, lang) {
  if (!obj) return obj;
  if (lang === 'en') return obj;
  
  // Deep clone to avoid mutating original state
  const newObj = JSON.parse(JSON.stringify(obj));
  
  const deeplyTranslate = (item) => {
    if (Array.isArray(item)) {
      item.forEach(deeplyTranslate);
    } else if (typeof item === 'object' && item !== null) {
      for (const key in item) {
        if (typeof item[key] === 'string') {
          // specific keys to translate
          if (['name', 'reason', 'occasion', 'vibe', 'shirt', 'pant', 'shoes', 'top', 'bottom', 'dupatta'].includes(key)) {
            item[key] = translateBackendText(item[key], lang);
          } else if (['style_tips', 'summary', 'colors'].includes(key)) {
             item[key] = translateBackendText(item[key], lang);
          } else if (['description', 'title'].includes(key)) {
             item[key] = translateBackendText(item[key], lang);
          }
        } else if (typeof item[key] === 'object') {
          deeplyTranslate(item[key]);
        }
      }
    }
  };
  
  deeplyTranslate(newObj);
  return newObj;
}
